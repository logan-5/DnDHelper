    @IBAction func savePlayerDetail(segue:UIStoryboardSegue) {
        if let playerDetailsViewController = segue.sourceViewController as? NewPlayerTableViewController {

            //add the new player to the players array
            if let player = playerDetailsViewController.player {
                CharacterData.players.append(player)

                //update the tableView
                let indexPath = NSIndexPath(forRow: CharacterData.players.count-1, inSection: 0)
                tableView.insertRowsAtIndexPaths([indexPath], withRowAnimation: .Automatic)
            }
        }
